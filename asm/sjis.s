# s32 Kanji_OffsetFromShiftJIS(s32 shiftJISCodepoint)
# 
# Returns the offset of the glyph texture data in `kanji` corresponding to a
# given 2-byte Shift-JIS codepoint. No range validity check is carried out.
#
# @param shiftJISCodepoint Codepoint of glyph.
# return s32 offset into `kanji` file.
BEGIN Kanji_OffsetFromShiftJIS
    # Characters with codepoints >= 0x8800 are kanji. Arrangement is regular, 
    # so convert index directly.
    li      $at, 0x8800
    slt     $at, $a0, $at
    bnez    $at, .not_kanji
     # 0xBC is the size of one block of glyphs in `kanji`:
     # 0x100 possible codepoints with the same byte1
     # - 0x40 unused at beginning 
     # - 1 unused at 0x7F 
     # - 3 unused at 0xFD, 0xFE, 0xFF
     li     $a2, 0xBC
    # Get byte1 and adjust so starts at 0
    srl     $a1, $a0, 8
    addi    $a1, $a1, -0x88
    multu   $a2, $a1
    # Get byte2 and adjust so starts at 0
    andi    $a3, $a0, 0xff
    addi    $a3, $a3, -0x40
    slti    $at, $a3, 0x40
    mflo    $a2
    # 0xXX7F is always empty and elided in the file, so decrement
    bnezl   $at, .kanji_lower_block
     mflo   $a2
    addi    $a3, $a3, -1
    mflo    $a2
.kanji_lower_block:
    addi    $a3, $a3, 0x30a
    add     $a3, $a3, $a2
    jr      $ra
     sll    $v0, $a3, 7
     # returns (0x30A + (adjusted byte2) + (adjusted byte1) * 0xBC) * FONT_CHAR_TEX_SIZE

# Non-kanji are arranged with irregular gaps, use the lookup table.
.not_kanji:
    # Get byte1 and adjust so starts at 0
    srl     $a1, $a0, 8
    addi    $a1, $a1, -0x81
    multu   $a2, $a1
    # Get byte2 and adjust so starts at 0
    andi    $a3, $a0, 0xff
    addi    $a3, $a3, -0x40
    slti    $at, $a3, 0x40
    mflo    $a2
    # 0xXX7F is always empty and elided in the file, so decrement
    bnezl   $at, .not_kanji_lower_block
     mflo   $a2
    addi    $a3, $a3, -1
    mflo    $a2
.not_kanji_lower_block:
    add     $a3, $a3, $a2
    lui     $a2, %hi(kanjiNonKanjiIndices)
    sll     $a3, $a3, 1
    addiu   $a2, %lo(kanjiNonKanjiIndices)
    add     $a3, $a3, $a2
    lh      $a2, ($a3)
    jr      $ra
     sll    $v0, $a2, 7
     # returns kanjiNonKanjiIndices[(adjusted byte2) + (adjusted byte1) * 0xBC] * FONT_CHAR_TEX_SIZE
END Kanji_OffsetFromShiftJIS

